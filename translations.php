// Helper functie om een vertaling op te halen
function t($key) {
    global $translations;
    $lang = $_COOKIE['siteLanguage'] ?? 'be-nl'; // cookie bepaalt taal, fallback be-nl
    return $translations[$lang][$key] ?? $key;
}

function t2($key, $lang = 'be-nl', $placeholders = []) {
    global $translations;

    // Zorg dat key en lang strings zijn
    $key = (string)$key;
    $lang = (string)$lang;

    // Vertaling ophalen
    $text = isset($translations[$lang][$key]) ? $translations[$lang][$key] : $key;

    // Placeholders vervangen
    if (is_array($placeholders)) {
        foreach ($placeholders as $placeholder => $value) {
            // Let op: placeholder moet string zijn
            $text = str_replace('{' . (string)$placeholder . '}', $value, $text);
        }
    }

    return $text;
}

function t3($key, $lang = 'be-nl', $localTranslations = null) {
    // Als er geen expliciete translations-array gegeven is: return key (geen globale fallback)
    if (!is_array($localTranslations) || empty($localTranslations)) {
        return (string)$key;
    }

    // Zorg ervoor dat key en lang strings zijn
    $key = (string)$key;
    $lang = (string)$lang;

    // Zoek naar de vertaling in de tijdelijke array
    $text = isset($translations[$lang][$key]) ? $translations[$lang][$key] : $key;

    return $text;
}
